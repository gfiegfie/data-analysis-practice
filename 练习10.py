"""
中位数

手工计算（奇数个）

数据集：[1, 3, 5] → 中间位置是第2个 → 中位数 = 3

步骤 3.2.2：手工计算（偶数个）

数据集：[1, 2, 3, 4] → 中间两个是 2 和 3 → (2+3)/2 = 2.5
"""

# 翻译：导入 numpy 库，并给它起一个别名 np。
# 解释：numpy 是一个用于数值计算的第三方库，提供了数组、数学函数等。as np 是为了后续书写方便
import numpy as np

# 翻译：创建一个名为 data_odd 的列表，包含三个整数：1, 3, 5。
# 解释：这个列表的元素个数是奇数（3个），用于演示中位数计算
data_odd = [1, 3, 5]
# 翻译：创建一个名为 data_even 的列表，包含四个整数：1, 2, 3, 4。
# 解释：这个列表的元素个数是偶数（4个），中位数取中间两个数的平均值
data_even = [1, 2, 3, 4]
# 翻译：调用 numpy 中的 median 函数，计算 data_odd 的中位数，并将结果打印到屏幕。
# 解释：对于奇数个数的列表，中位数是排序后位于中间的数。[1,3,5] 的中位数是 3.0
print(np.median(data_odd))
# 翻译：调用 numpy 中的 median 函数，计算 data_even 的中位数，并将结果打印到屏幕。
# 解释：对于偶数个数的列表，中位数是中间两个数的平均值。[1,2,3,4] 的中间两个数是 2 和 3，平均值为 2.5
print(np.median(data_even))
