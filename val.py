# 生成示例销售数据，保存为 salex.xlsx
import pandas as pd

# 创建一个简单的数据集
data = {
    "商品名称": ["苹果", "香蕉", "橘子", "葡萄", "西瓜", "芒果", "草莓", "菠萝"],
    "销量": [120, 95, 80, 110, 60, 75, 130, 45],
    "单价": [5.0, 3.5, 4.0, 8.0, 12.0, 10.0, 15.0, 7.5],
}
df = pd.DataFrame(data)
df["销售额"] = df["销量"] * df["单价"]  # 计算销售额列

# 保存为 Excel 文件
df.to_excel("销售数据.xlsx", index=False)
print("数据已生成并保存为销售数据.xlsx")
